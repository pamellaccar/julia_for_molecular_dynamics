É possível observar a proximidade da solução a partir do gradiente da função, no caso de minimização por derivadas,
se o gradiente for pequeno, pode ser um indicativo que o resultado está perto de um mínimo.

Ou, como resolvido na questão 44, ajustando o número de passos ou iterações.
Aumentar o número de passos pode permitir que o algoritmo explore mais detalhadamente o espaço de busca
É necessário escolher um número ideal de passos que depende da complexidade do problema pra que o cálculo 
não fique inviável computacionalmente.
